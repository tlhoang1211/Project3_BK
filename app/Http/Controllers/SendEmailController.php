    public function send(Request $request)
    {
        $this->validate($request, [
            'name' => 'required',
            'email' => 'required|email',
            'message' => 'required'
        ]);
        $data = array(
            'name' => $request->name,
            'message' => $request->message
        );
        Mail::to('t1908e@gmail.com')->send(new SendMail($data));
        return back()->with('success', 'Cám ơn bạn đã liên hệ với chúng tôi!');
    }

